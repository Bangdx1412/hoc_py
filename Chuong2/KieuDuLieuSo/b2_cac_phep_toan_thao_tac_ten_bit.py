# Các phép toán thao tác bit cơ bản
# Phép toán AND (&)
# Phép toán OR (|)
# Phép toán XOR (^)
# Phép toán NOT (~)
# Toán tử dịch bit 
# Dịch phải (>>) a/2 ^ n
# Dịch trái bit (<<) = a * 2^n vd a = 10 print(a<<3) => 10 * 2 ^ 3

a = 10
b = 2
print(a & b)
print(a | b)
print(a ^ b)
print(~b) # -(n+1)
print(a<<3)
print(a>>3)
